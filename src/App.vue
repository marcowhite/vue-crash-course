<template>
  <div id="app">
    <h1>Todo application</h1>
    <AddToDoComponent
        @add-todo = "addTodo"
    />
    <hr> 
    <ToDoList
        v-bind:todos = "todos"
        @remove-todo="removeTodo"
    />
  </div>
</template>

<script>
import ToDoList from "@/components/ToDoList"
import AddToDoComponent from '@/components/AddToDoComponent'
export default {
  name: 'App',
  data (){
    return{
      todos: [
          {id:1, title:'Купить сыру', completed:false},
          {id:2, title:'Купить ноги', completed:false},
          {id:3, title:'Купить Алексея Навального', completed:false}
      ]
    }
  },
  mounted() {
    fetch('https://jsonplaceholder.typicode.com/todos?_limit=3')
        .then(response => response.json())
        .then(json => {
          this.todos = json})
  },
  methods:{
    removeTodo(id){
      this.todos = this.todos.filter(t => t.id !== id)
    },
    addTodo(todo){
      this.todos.push(todo)
    }
  },
  components: {
    ToDoList, AddToDoComponent
  }
}
</script>

